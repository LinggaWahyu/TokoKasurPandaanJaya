package com.equinox.model;

/**
 *
 * @author equinox
 */
public class LogLogin {
    
    private String id, nama_user, waktu_login;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNama_user() {
        return nama_user;
    }

    public void setNama_user(String nama_user) {
        this.nama_user = nama_user;
    }

    public String getWaktu_login() {
        return waktu_login;
    }

    public void setWaktu_login(String waktu_login) {
        this.waktu_login = waktu_login;
    }
    
    
}
